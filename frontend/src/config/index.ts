export function getNetworkConfig(chainId: number) {
  switch (chainId) {
    case holesky.id:
      return {
        contracts: configs.holesky.CONTRACT,
        abis: ABIs.holesky
      }
    case polygon.id:
      return {
        contracts: configs.polygon.CONTRACT,
        abis: ABIs.polygon
      }
    default:
      throw new Error(`Unsupported chainId: ${chainId}`)
  }
}