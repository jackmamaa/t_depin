import { holesky, polygon } from 'viem/chains'
import { createPublicClient, http } from 'viem'

type PublicClientConfig = {
  [key: string]: ReturnType<typeof createPublicClient>
}

const publicClients: PublicClientConfig = {
  holesky: createPublicClient({
    chain: holesky,
    transport: http()
  }),
  polygon: createPublicClient({
    chain: polygon,
    transport: http()
  })
}
  
export const getPublicClient = (chainId: number) => {
  switch (chainId) {
    case holesky.id:
      return publicClients.holesky
    case polygon.id:
      return publicClients.polygon
    default:
      throw new Error(`Unsupported chainId: ${chainId}`)
  }
}
  
